package com.example.cashflowapp;

public class GoldRecord {
    private int id;
    private String goldType;
    private int goldAmount;

    public int getId() { return id; }

    public void setId(int id) { this.id = id; }

    public String getGoldType() { return goldType; }

    public void setGoldType(String goldType) { this.goldType = goldType; }

    public int getGoldAmount() { return goldAmount; }

    public void setGoldAmount(int goldAmount) { this.goldAmount = goldAmount; }
}
